package oop.polymorphism;

public class Pets {

    public void eat() {
        System.out.println("Ом-ном");
    }

    public void sniff() {
        System.out.println("Шмыг-шмыг");
    }

    public void breath() {
        System.out.println("Вздох-вздох");
    }

    public void move() {
        System.out.println("Топ-топ");
    }

    public void look() {
        System.out.println("Зырк-зырк");
    }

}
